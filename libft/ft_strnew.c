#include "libft.h"

char		*ft_strnew(size_t size)
{
	char	*c;

	c = (char *)malloc(sizeof(char) * (size + 1));
	if (c == 0)
		return (0);
	ft_memset(c, '\0', size + 1);
	return (c);
}
