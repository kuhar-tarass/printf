#include "libft.h"

void		ft_putnbr_fd(int n, int fd)
{
	char	buf[12];
	int		i;
	int		pos;

	i = 9;
	pos = 1;
	ft_memset(buf, '\0', 12);
	if (n < 0)
		pos = -1;
	buf[10] = (n % 10) * pos + 48;
	n = n / (10 * pos);
	while (n > 0)
	{
		buf[i--] = n % 10 + 48;
		n = n / 10;
	}
	buf[i] = (pos != -1) ? '\0' : '-';
	i = -1;
	while (++i < 11)
		if (buf[i] != '\0')
			write(fd, &buf[i], 1);
}
